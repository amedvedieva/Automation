package at.lesson1;

public class Calculator {
    public int plus(int a, int b) {
        return a + b;
    }

    public long minus(long a, long b) {
        return a - b;
    }

    public float multiplication(float a, float b) {
        return a * b;
    }

    public double division(double a, double b) {
        return a / b;
    }
}
